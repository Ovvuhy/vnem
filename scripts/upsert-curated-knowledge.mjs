function profileFor(entry) {
  const bestFor = entry.best_for.slice(0, 3).map((item) => `- ${item}`).join("\n");
  const recommendations = entry.recommended_when.slice(0, 3).map((item) => `- ${item}`).join("\n");
  const risks = entry.risk_flags.length ? entry.risk_flags.join(", ") : "none recorded";
  const sources = entry.source_urls.slice(0, 4).map((item) => `- ${item}`).join("\n");

  return `# ${entry.name}

${entry.summary_llm}

## Best For

${bestFor}

## Recommended When

${recommendations}

## Review Notes

Trust tier: ${entry.trust_tier}. Review status: ${entry.review_status}. Permissions: ${entry.permissions.join(", ") || "none recorded"}. Risk flags: ${risks}.

Sources:
${sources}
`;
}

for (const entry of entries) {
  const dir = path.join(ENTRIES_DIR, entry.slug);
  await mkdir(dir, { recursive: true });
  await writeFile(path.join(dir, "entry.yaml"), yaml.dump(entry, {
    lineWidth: 100,
    noRefs: true,
    sortKeys: false
  }));
  await writeFile(path.join(dir, "profile.md"), profileFor(entry));
}

